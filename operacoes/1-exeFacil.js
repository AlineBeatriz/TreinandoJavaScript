let a = 20;
let b = 50;
calculo = a + b;
console.log("Soma: " + calculo);

// 2º Subtraia dois números e exiba o resultado.
let c = 100;
let d = 50;
calculo = c - d;
console.log("Subtração: " + calculo);

// 3º Multiplique dois números e exiba o resultado.
let e = 9;
let f = 11;
calculo = e * f;
console.log("Multiplicação: " + calculo);

// 4º Divida dois números e exiba o resultado.
let g = 65;
let h = 30;
calculo = g / h;
console.log("Dividir: " + calculo);

// 5º Calcule o dobro de um número.
let i = 20;
calculo = 2 * i;
console.log("O dobro de " + i + " é: " + calculo);

// 6º Calcule a média entre dois números.
let j = 10;
